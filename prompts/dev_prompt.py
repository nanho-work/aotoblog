# prompts/dev_prompt.py
def get_prompt(topic, summarized_context):
    return f"""
주제: {topic}
참고 요약: {summarized_context}

아래 조건을 반드시 지켜 JSON 형식으로만 출력해.
JSON 이외의 불필요한 설명, 지문 복사, 안내 문구는 절대 넣지 마라.
주제와 참고 요약은 글을 쓰기 위한 자료일 뿐, 본문에 그대로 출력하면 안 된다.

출력 형식:
{{
"title": "SEO 친화적이고 클릭을 유도하는 제목 (70자 이내)",
"content": "본문 (최종적으로 3000~4000자, 반드시 마크다운 형식)\\n"
            "최우선 조건: 3000자 미만이면 이어서 작성하여 반드시 3000~4000자로 맞출 것.\\n"
            "구성 지침:\\n"
            "- 글은 H1 제목(# )으로 시작할 것.\\n"
            "- 3~5개 H2 소제목 사용. 각 소제목 아래에 구체적 설명 포함.\\n"
            "- 필요시 코드 블록(```)과 리스트(1. 또는 •) 활용.\\n"
            "- 불필요 반복 금지.\\n"
            "- 결론에서는 핵심 요약 후 질문형 문장으로 마무리.\\n"
            "스타일 지침:\\n"
            "- 블로그 글처럼 깔끔하고 친근한 존댓말 사용.\\n"
            "- 과한 광고·격언 톤 금지, 실용 사례 중심.\\n"
            "- SEO 키워드(3~5개)를 자연스럽게 반복.\\n"
            "- 최신 흐름·사례·데이터 반영(출처는 일반 문장으로 언급).\\n",
"tags": [
    "주제와 직접 관련된 SEO 키워드 10개"
]
}}
"""